b = "Hello, World!"
print(b[2:5]) # Get the characters from position 2 to position 5 (not included)

a = "Hello, World!"
print(a.upper()) #The upper() method returns the string in upper case

a = "Hello, World!"
print(a.lower()) #The lower() method returns the string in lower case

a = " Hello, World! "
print(a.strip()) #he strip() method removes any whitespace from the beginning or the end

a = "Hello, World!"
print(a.replace("H", "J")) #The replace() method replaces a string with another string

a = "Hello, World!"
print(a.split(",")) # The split() method splits the string into substrings if it finds instances of the separator